package Introduction.Operators;

public class Assignment {
    public static void main(String[] args) {
        int x = 10; 
        System.out.println("Value of x: " + x);
        System.out.println(x/=2);
        System.out.println(x+=2);
        System.out.println(x-=3);
        System.out.println(x*=4);
        System.out.println(x >>=2);
        System.out.println(x<<=2);
    }
    
}
