package Introduction.Operators;

public class Logical {

    public static void main(String[] args) {
        int a=2,b=3,c=5;
       
        System.out.println(a<b && c>b);
        System.out.println(a>b || b<c);

      //Bitwise:

      System.out.println(a&b);
      System.out.println(c|b);

    }
    
}
